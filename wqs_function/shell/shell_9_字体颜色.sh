# 打印信息的字体颜色和字体设置，格式"\033[控制代码 内容 \033[控制代码"，控制代码和第二个"\033"和内容之间可以没有空格
# 这里需要注意的有两点：
# 1、当控制代码需要同时设置多个属性时，多个设置间用";"间隔，且只有最后一个属性能带"m"，前面的属性，如果有"m"则省略
# 2、如果设置只是临时的，第二个控制代码最好是"0m"，否则设置就会持续生效，其他的设置可以放在第一个控制代码里
# 3、光标的设置智能放在第二个控制代码里，不能放在第一个控制代码里,虽然光标的设置能生效，但是字体颜色的设置会失效

